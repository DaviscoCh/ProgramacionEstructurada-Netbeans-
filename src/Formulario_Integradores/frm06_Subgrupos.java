    public void informarMayorMultiploDe5() {
        for (int i = 1; i <= 3; i++) {
            List<Integer> subgrupoActual = obtenerSubgrupo(i);

            int mayorMultiploDe5 = encontrarMayorMultiploDe5(subgrupoActual);

            if (mayorMultiploDe5 != -1) {
                JOptionPane.showMessageDialog(null, "El mayor múltiplo de 5 en el Subgrupo " + i + " es: " + mayorMultiploDe5);
            } else {
                JOptionPane.showMessageDialog(null, "No hay múltiplos de 5 en el Subgrupo " + i);
            }
        }
    }

    private int encontrarMayorMultiploDe5(List<Integer> subgrupo) {
        int mayorMultiploDe5 = -1;

        for (int numero : subgrupo) {
            if (numero % 5 == 0 && numero > mayorMultiploDe5) {
                mayorMultiploDe5 = numero;
            }
        }

        return mayorMultiploDe5;
    }

