    public void informarSubgrupoConMayorProporcionNegativos() {
        double maxProporcionNegativos = -1;
        int subgrupoMaxPropNegativos = -1;

        for (int i = 1; i <= 3; i++) {
            List<Integer> subgrupoActual = obtenerSubgrupo(i);

            double proporcionNegativos = calcularProporcionNegativos(subgrupoActual);

            if (proporcionNegativos > maxProporcionNegativos) {
                maxProporcionNegativos = proporcionNegativos;
                subgrupoMaxPropNegativos = i;
            }
        }

        JOptionPane.showMessageDialog(null, "El subgrupo con la mayor proporción de números negativos es el Subgrupo "
                + subgrupoMaxPropNegativos + " con una proporción de: " + maxProporcionNegativos);
    }

    private double calcularProporcionNegativos(List<Integer> subgrupo) {
        int totalNegativos = 0;
        int totalNumeros = subgrupo.size();

        for (int numero : subgrupo) {
            if (numero < 0) {
                totalNegativos++;
            }
        }

        if (totalNumeros == 0) {
            return 0; // Evitar división por cero si el subgrupo está vacío
        }

        return (double) totalNegativos / totalNumeros;
    }

