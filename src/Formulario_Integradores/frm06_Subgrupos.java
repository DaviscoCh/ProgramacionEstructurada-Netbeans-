    public void informarSubgrupoConPrimoMasGrande() {
        int maxPrimo = -1;
        int subgrupoMaxPrimo = -1;
        for (int i = 0; i < 3; i++) {
            List<Integer> subgrupoActual = obtenerSubgrupo(i + 1);

            int primoMasGrande = encontrarPrimoMasGrande(subgrupoActual);

            if (primoMasGrande > maxPrimo) {
                maxPrimo = primoMasGrande;
                subgrupoMaxPrimo = i + 1;
            }
        }
        JOptionPane.showMessageDialog(null, "El subgrupo con el número primo más grande es el Subgrupo " + subgrupoMaxPrimo
                + " con el número primo: " + maxPrimo);
    }

    private List<Integer> obtenerSubgrupo(int numero) {
        switch (numero) {
            case 1:
                return Subconjunto;
            case 2:
                return Subconjunto2;
            case 3:
                return Subconjunto3;
            default:
                return new ArrayList<>();
        }
    }

    private int encontrarPrimoMasGrande(List<Integer> subgrupo) {
        int maxPrimo = -1;
        int subgrupoMaxPrimo = -1;
        for (int numero : subgrupo) {
            if (esPrimo(numero) && numero > maxPrimo) {
                maxPrimo = numero;
            }
        }
        return maxPrimo;
    }

    private boolean esPrimo(int numero) {
        if (numero <= 1) {
            return false;
        }

        for (int i = 2; i <= Math.sqrt(numero); i++) {
            if (numero % i == 0) {
                return false;
            }
        }
        return true;
    }

